package Lambda_Expression;

public class Comparator_Lambda {
int id;
String name;
double marks;
public Comparator_Lambda(int id, String name, double marks) {
	super();
	this.id = id;
	this.name = name;
	this.marks = marks;
}
@Override
public String toString() {
	return "Comparator_Lambda [id=" + id + ", name=" + name + ", marks=" + marks + "]";
}

}
