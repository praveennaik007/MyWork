package collection_Framework;

public class Array_List_P1 {
	int id;
	String name;
	double marks;
	public Array_List_P1(int id, String name, double d) {
		super();
		this.id = id;
		this.name = name;
		this.marks = d;
	}
	@Override
	public String toString() {
		return "Array_List_P1 [id=" + id + ", name=" + name + ", marks=" + marks + "]";
	}
	
}
