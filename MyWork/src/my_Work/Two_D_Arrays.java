package my_Work;

import java.util.Arrays;

public class Two_D_Arrays {
	public static void main(String[] args) {
		int a[][] = { {} };
		System.out.println(Arrays.toString(a[0]));

		int b[][] = new int[1][1];
		System.out.println(Arrays.toString(b[0]));

		int c[][] = new int[][] { {} };
		System.out.println(Arrays.toString(c[0]));
	}
}
